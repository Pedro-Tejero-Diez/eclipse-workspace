package S03T03N01PedroTejeroDiez;

public class Floristeria {

	private String nombre;
	private Arbol[] arboles;
	private Flor[] flores;
	private Decoracion[] decoracion;
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Arbol[] getArboles() {
		return arboles;
	}
	public void setArboles(Arbol[] arboles) {
		this.arboles = arboles;
	}
	public Flor[] getFlores() {
		return flores;
	}
	public void setFlores(Flor[] flores) {
		this.flores = flores;
	}
	public Decoracion[] getDecoracion() {
		return decoracion;
	}
	public void setDecoracion(Decoracion[] decoracion) {
		this.decoracion = decoracion;
	}
	
	
}
